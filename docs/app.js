/**
 * Remove ### MCP configuration (and body) so it is not folded into Installation (Cursor).
 * Aligns with scripts/generate_collection_pages.py::extract_mcp_configuration_section.
 */
function extractMcpConfigurationSection(deploy) {
    const text = String(deploy);
    const re = /(?:^|\n)(### MCP configuration\s*\n[\s\S]*?)(?=\n### |\s*$)/;
    const m = text.match(re);
    if (!m) {
        return { rest: text, mcp: '' };
    }
    const full = m[0];
    const start = text.indexOf(full);
    const rest = (text.slice(0, start) + text.slice(start + full.length)).trim();
    const mcp = m[1].trim();
    return { rest, mcp };
}

/**
 * Split deploy_and_use (after optional MCP extraction) into prose before first
 * "### Installation (...)" and per-platform sections.
 * Aligns with scripts/generate_collection_pages.py::split_deploy_into_sections.
 */
function splitDeployIntoSections(deployText) {
    const text = String(deployText);
    const pattern = /### Installation \((Claude Code|Cursor|Open Code)\)\s*\n([\s\S]*?)(?=### Installation \(|\s*$)/g;
    const all = Array.from(text.matchAll(pattern));
    if (all.length === 0) {
        return { pre: text.trim(), sections: [] };
    }
    const preEnd = text.indexOf('### Installation (');
    const pre = preEnd > 0 ? text.slice(0, preEnd).trim() : '';
    return {
        pre,
        sections: all.map(m => ({ platform: m[1], content: m[2].trim() }))
    };
}

